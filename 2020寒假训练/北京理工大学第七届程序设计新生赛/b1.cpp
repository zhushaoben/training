using namespace std;
int main(){
	for(int i=3000;i<=9999;i++)
		for(int j=0;j<=99;j++)
			for(int k=0;k<=99;k++){
				long long x=i*10000+j*100+k,x1=x*x;
				if(x1%10==1&&(x1%1000)/100==9&&(x1%100000)/10000==2&&(x1%10000000000000ll)/1000000000000ll==1
				&&(x1%10000000)/1000000==6&&(x1%1000000000)/100000000==0&&(x1%100000000000ll)/10000000000ll==8){
					printf("%lld %lld\n",x,x1);
				}
			}
	return 0;
} 
